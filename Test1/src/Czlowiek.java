public class Czlowiek {
    private String name;
    private int age;


    public Czlowiek(String name) {
        this.name = name;
    }

    public Czlowiek(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }



    public int getAge() {
        return age;
    }
}
